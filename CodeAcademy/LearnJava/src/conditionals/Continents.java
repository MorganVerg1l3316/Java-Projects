package testing;

public class Continents {
	public static void main(String[] args) {

	    int continent = 4;

	    switch (continent) {
	      case 1:
	       System.out.println("North America: Mexico City, Mexico.");
	       break;
	      case 2:
	       System.out.println("South America: Sao Paulo, Brazil");
	       break;
	      default:
	        System.out.println("Undefined continent!");
	        break; 
	    }
	  }
}
